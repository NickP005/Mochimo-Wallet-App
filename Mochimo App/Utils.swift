extension Array where Element==UInt8 {
    func ull_to_bytes(_ outlen: Int) -> [UInt8] {
        var out_array: [UInt8] = []
        out_array.reserveCapacity(outlen)
        for i in (0...outlen-1).reversed() {
            out_array.append(self[safe: i] ?? 0)
        }
        return out_array
    }
    
    ///This will read the bytes as BigEndian and convert them to Int
    ///
    /// - warning: Make sure to convert the bytes to BigEndian!
    func toUInt() -> UInt {
        var value : UInt = 0
        for byte in self {
            value = value << 8
            value = value | UInt(byte)
        }
        return value
    }
}

